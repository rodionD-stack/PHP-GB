<?php

    $a = 10;
    $b = 0;

    do{
        if($b == 0) {
            echo $b . ' ' . '-' . ' ' . 'ноль' . '<br/>';
        } else if($b % 2 == 0) {
            echo $b . ' ' . '-' . ' ' . 'четное число' . '<br/>';
        } else if($b % 2 !== 0) {
            echo $b . ' ' . '-' . ' ' . 'не четное число' . '<br/>';
        }
        $b++;
    }while($b <= $a);

?>